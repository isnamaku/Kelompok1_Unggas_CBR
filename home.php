<h2 class="art-postheader">Beranda Sistem Pakar Diagnosa Penyakit pada Unggas</h2>
<div class="art-postcontent">
<p><strong>Sistem Pakar Diagnosa Penyakit pada Unggas, merupakan sistem pakar untuk membantu memberikan Informasi dini mengenali gejala-gejala penyakit pada unggas</strong></p>
<p style="font-family:'Verdana', Courier, monospace; color:#336; text-align:justify"><img src="images/unggas_home.jpg" width="350" height="280" style="float:right; " /> 
</br>
</br>
Sebagai seorang pembisnis perlu mempunyai kewaspadaan yang tinggi terhadap penyakit yang mungkin menjangkiti hewan ternaknya, seperti halnya pada unggas. Untuk itu, adanya Sistem Pakar Diagnosa Penyakit pada Unggas yang menggunakan metode Case Based Reasoning (CBR) dapat dijadikan salah satu solusi. Sistem ini mampu melakukan diagnosis penyakit pada unggas dengan perhitungan yang akurat dan tepat.
</p>
